function toEntries(items: WordTuple[], startRank: number): VocabularyEntry[] {
  return items.map(([word, chinese, example], index) => ({
    word,
    phonetic: '',
    pos: 'v.',
    chinese,
    definition: chinese,
    core: chinese,
    example,
    collocations: [],
    rank: startRank + index,
  }))
}

